package beans;


import org.springframework.stereotype.Component;

@Component
public class Book {
    private String name = "Algorithms";

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Book{" +
                "name='" + name + '\'' +
                '}';
    }
}
